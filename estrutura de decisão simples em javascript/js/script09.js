let diaDaSemana = parseInt(prompt("digite um numero para saber o dia da semana"))

switch (diaDaSemana) {
    case 1:
        alert("domingo")
        
        break;

        case 2:
        alert("segunda-feira")
        
        break;

        case 3:
        alert("terça-feira")
        
        break;

        case 4:
        alert("quarta-feira")
        
        break;

        case 5:
        alert("quinta-feira")
        
        break;

        case 6:
        alert("sexta-feira")
        
        break;

        case 7:
        alert("sabado")
        
        break;
    alert("não há dia correspondente")
        break;
}